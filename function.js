//Example 2 => Function with argument and without return type
function fun(a,b)
{
    console.log("Function with argument and without return type")
    for(var i=a;i<b;i++)
    {
        if(i%2==0)
    {
        console.log("The Number ",i," is EVEN")
    }
    else
    {
        console.log("The Number ",i," is ODD")
    }
    } 
}
fun(5,10)

